package model;

/**
 *
 * @author maclife
 */
public class Voucher {
    private int id;
    private String code;

    public Voucher() {
    }

    public Voucher(int id, String code) {
        this.id = id;
        this.code = code;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    @Override
    public String toString() {
        return "Voucher{" + "id=" + id + ", code=" + code + '}';
    }
}
